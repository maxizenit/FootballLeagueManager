package ru.maxizenit.footballleaguemanager.enm;

/**
 * Перечисление {@code Result} представляет результаты матча для конкретной команды.
 */
public enum Result {

  /**
   * Победа.
   */
  WIN,

  /**
   * Поражение.
   */
  LOSS,

  /**
   * Ничья.
   */
  DRAW,

  /**
   * Результата нет.
   */
  NO_RESULT
}
