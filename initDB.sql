-- Создание базы данных

CREATE DATABASE "FootballLeagueManagerDB"
    WITH
    OWNER = postgres
    ENCODING = 'UTF8'
    LC_COLLATE = 'Russian_Russia.1251'
    LC_CTYPE = 'Russian_Russia.1251'
    TABLESPACE = pg_default
    CONNECTION LIMIT = -1
    IS_TEMPLATE = False;

-- Создание таблиц
-- Таблица позиций

CREATE TABLE IF NOT EXISTS public."position"
(
    id serial NOT NULL,
    index_number integer NOT NULL,
    code text COLLATE pg_catalog."default" NOT NULL,
    name text COLLATE pg_catalog."default" NOT NULL,
    CONSTRAINT position_pkey PRIMARY KEY (id)
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public."position"
    OWNER to postgres;
	
-- Таблица команд

CREATE TABLE IF NOT EXISTS public.team
(
    id serial NOT NULL,
    code text COLLATE pg_catalog."default" NOT NULL,
    name text COLLATE pg_catalog."default" NOT NULL,
    CONSTRAINT team_pkey PRIMARY KEY (id)
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public.team
    OWNER to postgres;
	
-- Таблица игроков

CREATE TABLE IF NOT EXISTS public.player
(
    id serial NOT NULL,
    team_id integer,
    position_id integer NOT NULL,
    "number" integer,
    last_name text COLLATE pg_catalog."default" NOT NULL,
    first_name text COLLATE pg_catalog."default" NOT NULL,
    short_name text COLLATE pg_catalog."default",
    birthdate date NOT NULL,
    CONSTRAINT player_pkey PRIMARY KEY (id),
    CONSTRAINT position_id_fkey FOREIGN KEY (position_id)
        REFERENCES public."position" (id) MATCH SIMPLE
        ON UPDATE CASCADE
        ON DELETE CASCADE
        NOT VALID,
    CONSTRAINT team_id_fkey FOREIGN KEY (team_id)
        REFERENCES public.team (id) MATCH SIMPLE
        ON UPDATE CASCADE
        ON DELETE SET NULL
        NOT VALID
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public.player
    OWNER to postgres;
	
CREATE INDEX IF NOT EXISTS fki_position_id_fkey
    ON public.player USING btree
    (position_id ASC NULLS LAST)
    TABLESPACE pg_default;
	
CREATE INDEX IF NOT EXISTS fki_team_id_fkey
    ON public.player USING btree
    (team_id ASC NULLS LAST)
    TABLESPACE pg_default;
	
-- Таблица матчей

CREATE TABLE IF NOT EXISTS public.match
(
    id serial NOT NULL,
    date date NOT NULL,
    home_team_id integer NOT NULL,
    guest_team_id integer NOT NULL,
    played boolean,
    technical_defeat_winner_id integer,
    CONSTRAINT match_pkey PRIMARY KEY (id),
    CONSTRAINT home_team_id_fkey FOREIGN KEY (home_team_id)
        REFERENCES public.team (id) MATCH SIMPLE
        ON UPDATE CASCADE
        ON DELETE CASCADE
        NOT VALID,
    CONSTRAINT guest_team_id_fkey FOREIGN KEY (guest_team_id)
        REFERENCES public.team (id) MATCH SIMPLE
        ON UPDATE CASCADE
        ON DELETE CASCADE
        NOT VALID
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public.match
    OWNER to postgres;

CREATE INDEX IF NOT EXISTS fki_first_team_id_fkey
    ON public.match USING btree
    (home_team_id ASC NULLS LAST)
    TABLESPACE pg_default;

CREATE INDEX IF NOT EXISTS fki_second_team_id_fkey
    ON public.match USING btree
    (guest_team_id ASC NULLS LAST)
    TABLESPACE pg_default;
	
-- Таблица записей из состава

CREATE TABLE IF NOT EXISTS public.squad
(
    id serial NOT NULL,
    match_id integer NOT NULL,
    team_id integer NOT NULL,
    player_id integer NOT NULL,
    CONSTRAINT squad_pkey PRIMARY KEY (id),
    CONSTRAINT match_id_fkey FOREIGN KEY (match_id)
        REFERENCES public.match (id) MATCH SIMPLE
        ON UPDATE CASCADE
        ON DELETE CASCADE
        NOT VALID,
    CONSTRAINT player_id_fkey FOREIGN KEY (player_id)
        REFERENCES public.player (id) MATCH SIMPLE
        ON UPDATE CASCADE
        ON DELETE CASCADE
        NOT VALID,
    CONSTRAINT team_id_fkey FOREIGN KEY (team_id)
        REFERENCES public.team (id) MATCH SIMPLE
        ON UPDATE CASCADE
        ON DELETE CASCADE
        NOT VALID
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public.squad
    OWNER to postgres;

CREATE INDEX IF NOT EXISTS fki_m
    ON public.squad USING btree
    (team_id ASC NULLS LAST)
    TABLESPACE pg_default;

CREATE INDEX IF NOT EXISTS fki_player_id_fkey
    ON public.squad USING btree
    (player_id ASC NULLS LAST)
    TABLESPACE pg_default;
	
-- Таблица голов
	
CREATE TABLE IF NOT EXISTS public.goal
(
    id serial NOT NULL,
    match_id integer NOT NULL,
    team_id integer NOT NULL,
    minute integer NOT NULL,
    injury_minute integer,
    scorer_id integer NOT NULL,
    assistant_id integer,
    penalty boolean,
    own_goal boolean,
    CONSTRAINT goal_pkey PRIMARY KEY (id),
    CONSTRAINT assistant_id_fkey FOREIGN KEY (assistant_id)
        REFERENCES public.player (id) MATCH SIMPLE
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT match_id_fkey FOREIGN KEY (match_id)
        REFERENCES public.match (id) MATCH SIMPLE
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT scorer_id_fkey FOREIGN KEY (scorer_id)
        REFERENCES public.player (id) MATCH SIMPLE
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT team_id_fkey FOREIGN KEY (team_id)
        REFERENCES public.team (id) MATCH SIMPLE
        ON UPDATE CASCADE
        ON DELETE CASCADE
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public.goal
    OWNER to postgres;

CREATE INDEX IF NOT EXISTS fki_assistant_id_fkey
    ON public.goal USING btree
    (assistant_id ASC NULLS LAST)
    TABLESPACE pg_default;

CREATE INDEX IF NOT EXISTS fki_match_id_fkey
    ON public.goal USING btree
    (match_id ASC NULLS LAST)
    TABLESPACE pg_default;

CREATE INDEX IF NOT EXISTS fki_p
    ON public.goal USING btree
    (team_id ASC NULLS LAST)
    TABLESPACE pg_default;

CREATE INDEX IF NOT EXISTS fki_scorer_id_fkey
    ON public.goal USING btree
    (scorer_id ASC NULLS LAST)
    TABLESPACE pg_default;
	

